// Helper functions
export const getFishById = (id: string): Fish | undefined => {
  return fishDatabase.find(fish => fish.id === id);
};

export const getFreshwaterFish = (): Fish[] => {
  return fishDatabase.filter(fish => fish.waterType === 'freshwater');
};

export const getSaltwaterFish = (): Fish[] => {
  return fishDatabase.filter(fish => fish.waterType === 'saltwater');
};

export const getFishByTemperament = (temperament: Fish['temperament']): Fish[] => {
  return fishDatabase.filter(fish => fish.temperament === temperament);
};

export const getBeginnerFriendlyFish = (): Fish[] => {
  return fishDatabase.filter(fish => fish.careLevel === 'beginner');
};

export const searchFish = (query: string): Fish[] => {
  const lowerQuery = query.toLowerCase();
  return fishDatabase.filter(fish =>
    fish.commonName.toLowerCase().includes(lowerQuery) ||
    fish.scientificName.toLowerCase().includes(lowerQuery)
  );
};

export const filterFish = (options: {
  waterType?: Fish['waterType'];
  temperament?: Fish['temperament'];
  maxTankSize?: number;
  careLevel?: Fish['careLevel'];
}): Fish[] => {
  return fishDatabase.filter(fish => {
    if (options.waterType && fish.waterType !== options.waterType) return false;
    if (options.temperament && fish.temperament !== options.temperament) return false;
    if (options.maxTankSize && fish.minTankSize > options.maxTankSize) return false;
    if (options.careLevel && fish.careLevel !== options.careLevel) return false;
    return true;
  });
};
